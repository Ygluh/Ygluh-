#include <stdio.h>
#include <math.h>

// Структура для представления треугольника
typedef struct {
    float a;
    float b;
    float c;
} Треугольник;

// Функция для вычисления площади треугольника по формуле Герона
float площадь_треугольника(Треугольник t) {
    float p = (t.a + t.b + t.c) / 2;
    return sqrt(p * (p - t.a) * (p - t.b) * (p - t.c));
}

int main() {
    // Массив для хранения 3-х треугольников
    Треугольник треугольники[3];

    // Ввод данных для каждого треугольника
    for (int i = 0; i < 3; i++) {
        printf("Введите длины сторон треугольника %d:\n", i + 1);
        printf("a = ");
        scanf("%f", &треугольники[i].a);
        printf("b = ");
        scanf("%f", &треугольники[i].b);
        printf("c = ");
        scanf("%f", &треугольники[i].c);
    }

    // Поиск треугольника с наибольшей площадью
    int индекс_максимального = 0;
    float максимальная_площадь = площадь_треугольника(треугольники[0]);
    for (int i = 1; i < 3; i++) {
        float текущая_площадь = площадь_треугольника(треугольники[i]);
        if (текущая_площадь > максимальная_площадь) {
            максимальная_площадь = текущая_площадь;
            индекс_максимального = i;
        }
    }

    // Вывод результата
    printf("Треугольник с наибольшей площадью:\n");
    printf("a = %.2f, b = %.2f, c = %.2f\n", треугольники[индекс_максимального].a,
           треугольники[индекс_максимального].b, треугольники[индекс_максимального].c);
    printf("Площадь = %.2f\n", максимальная_площадь);

    return 0;
}
